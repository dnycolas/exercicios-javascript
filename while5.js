// 10. Faça um programa que receba um número do usuário e continue
// a pedir o número se o numero for diferente de 0.

let numero = parseInt(prompt("digite o numero 0"))


while (numero != 0) {
    let num = parseInt(prompt("digite o zero!!!"))
    numero = num
}
if (numero == 0) {
    console.log("boa!!!")      
}